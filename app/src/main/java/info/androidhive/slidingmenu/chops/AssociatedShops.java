public class AssociatedShops {

    int getId;
    String getName;
    String getCity;
    String getStreet;
    double getLatitude;
    double getLongitude;
    int getDeleted;

    public int getId() {
        return getId;
    }

    public void setId(int getId) {
        this.getId = getId;
    }

    public String getName() {
        return getName;
    }

    public void setName(String getName) {
        this.getName = getName;
    }

    public String getCity() {
        return getCity;
    }

    public void setCity(String getCity) {
        this.getCity = getCity;
    }

    public String getStreet() {
        return getStreet;
    }

    public void setStreet(String getStreet) {
        this.getStreet = getStreet;
    }

    public double getLatitude() {
        return getLatitude;
    }

    public void setLatitude(double getLatitude) {
        this.getLatitude = getLatitude;
    }

    public double getLongitude() {
        return getLongitude;
    }

    public void setLongitude(double getLongitude) {
        this.getLongitude = getLongitude;
    }

    public int getDeleted() {
        return getDeleted;
    }

    public void setDeleted(int getDeleted) {
        this.getDeleted = getDeleted;
    }
}
